def get_fig2(db_helper):
    """
    获取数据
    :return:
    """
    res = db_helper.pd_read_sql("select * from rlt_police_case_num_predict")
    res['true_value'] = res['true_value'].apply(lambda x: int(x) if not math.isnan(x) else pd.NA)
    grouped_res = res.groupby(by=["case_type", "pre_value", "pre_value_high", "pre_value_low"])
    month_days = [31, 28, 31, 30]
    for label, group_df in grouped_res:
        j = 0
        for i, (row_index, row) in enumerate(group_df.iterrows()):
            if i+1 > sum(month_days[:j+1]):
                j += 1
            i = i - sum(month_days[:j])
            res.loc[row_index, 'case_time'] = '2018' + f'-0{j+1}-' + (f'0{i+1}' if i+1 < 10 else f'{i+1}')
    return res


